//Storing World Places Names in an Array
let countries:string[]=['Pakistan','China','Turkey','Iran','Bangladesh'];

//Printing Original Array
console.log(countries);

//Printing an Array in Alphabetical Order
console.log([...countries].sort());

//Showing Original Array
console.log(countries);

//Printing an Array in Reverse Alphabetical Order
console.log([...countries].reverse());

//Printing Again Original Array
console.log(countries);

//Printing Original Array in Reverse Order
console.log(countries.reverse());

//Printing Again Original Array in Reverse Order
console.log(countries.reverse());

//Printing an Array in Alphabetical Order
console.log(countries.sort());

//Sorting Again in Reverse Alphabetical Order
console.log(countries.reverse());

